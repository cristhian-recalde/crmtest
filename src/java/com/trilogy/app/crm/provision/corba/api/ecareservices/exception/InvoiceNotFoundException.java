package com.trilogy.app.crm.provision.corba.api.ecareservices.exception;

public class InvoiceNotFoundException extends Exception
{

    public InvoiceNotFoundException()
    {
        super();
    }

    public InvoiceNotFoundException(String arg0)
    {
        super(arg0);
    }

    public InvoiceNotFoundException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }

    public InvoiceNotFoundException(Throwable arg0)
    {
        super(arg0);
    }

}
